func TestRunReportStepSummary_TemplateEscapesURLAttributes(t *testing.T) {
	t.Parallel()

	var stdout, stderr bytes.Buffer
	code, err := runReportStepSummary(context.Background(), []string{
		"--html-url", `https://github.com/acme/project/actions/runs/1/artifacts/2?x=1&y=%22%3E%3Cscript%3Ealert(1)%3C/script%3E`,
		"--debug-url", `https://github.com/acme/project/actions/runs/1/artifacts/9?name=a&next=%27`,
	}, strings.NewReader(""), &stdout, &stderr)
	if err != nil {
		t.Fatalf("runReportStepSummary: %v", err)
	}
	if code != 0 {
		t.Fatalf("exit code: got %d, want 0", code)
	}
	got := stdout.String()
	if strings.Contains(got, `""><script>`) || strings.Contains(got, "<script>") {
		t.Fatalf("URL attribute was not safely escaped:\n%s", got)
	}
	for _, want := range []string{
		`x=1&amp;y=%22%3E%3Cscript%3Ealert%281%29%3C/script%3E`,
		`name=a&amp;next=%27`,
	} {
		if !strings.Contains(got, want) {
			t.Fatalf("missing escaped URL fragment %q in:\n%s", want, got)
		}
	}
}

func TestRunReportStepSummary_InvalidURL(t *testing.T) {
	t.Parallel()

	tests := []struct {
		name string
		args []string
		want string
	}{
		{name: "javascript", args: []string{"--html-url", "javascript:alert(1)"}, want: "unsupported scheme"},
		{name: "missing host", args: []string{"--debug-url", "https:///missing-host"}, want: "missing host"},
		{name: "malformed", args: []string{"--html-url", "https://[::1"}, want: "missing ']'"},
	}
	for _, tt := range tests {
		tt := tt
		t.Run(tt.name, func(t *testing.T) {
			t.Parallel()

			var stdout, stderr bytes.Buffer
			code, err := runReportStepSummary(context.Background(), tt.args, strings.NewReader(""), &stdout, &stderr)
			if code != 1 {
				t.Fatalf("exit code: got %d, want 1", code)
			}
			if err == nil || !strings.Contains(err.Error(), tt.want) {
				t.Fatalf("error: got %v, want containing %q", err, tt.want)
			}
		})
	}
}

