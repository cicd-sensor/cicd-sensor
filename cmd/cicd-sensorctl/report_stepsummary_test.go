func TestRunReportStepSummary_StdinHappyPath(t *testing.T) {
	t.Parallel()

	sample := sampleResultLog()
	sample.Hits[0].RuleID = "]((https://evil.example))"
	sample.Hits[0].RuleCondition = "![evil](https://evil.example)"
	sample.DomainObservations[0].Domain = "evil.example\n# forged"
	body, err := json.Marshal(sample)
	if err != nil {
		t.Fatalf("marshal sample: %v", err)
	}

	var stdout, stderr bytes.Buffer
	code, err := runReportStepSummary(context.Background(), []string{
		"--html-url", "https://github.com/acme/project/actions/runs/1/artifacts/2",
	}, bytes.NewReader(body), &stdout, &stderr)
	if err != nil {
		t.Fatalf("runReportStepSummary: %v", err)
	}
	if code != 0 {
		t.Fatalf("exit code: got %d, want 0", code)
	}

	got := stdout.String()
	for _, want := range []string{
		stepSummaryHeaderDetected,
		`<a href="https://github.com/acme/project/actions/runs/1/artifacts/2#hits">Open the full cicd-sensor report</a>`,
		`Generated by`,
	} {
		if !strings.Contains(got, want) {
			t.Fatalf("missing %q in output:\n%s", want, got)
		}
	}
	for _, unwanted := range []string{
		"evil.example",
		"curl-egress",
		"rule_condition",
		"forged",
	} {
		if strings.Contains(got, unwanted) {
			t.Fatalf("summary rendered untrusted result detail %q:\n%s", unwanted, got)
		}
	}
}

func TestRunReportStepSummary_ResultHeaders(t *testing.T) {
	t.Parallel()

	tests := []struct {
		name   string
		result string
		want   string
	}{
		{name: "no alert", result: resultdoc.ResultNoAlert, want: stepSummaryHeaderNoAlert},
		{name: "detected", result: resultdoc.ResultDetected, want: stepSummaryHeaderDetected},
		{name: "terminated", result: resultdoc.ResultTerminated, want: stepSummaryHeaderTerminated},
		{name: "unknown falls back to no alert", result: "javascript:alert(1)", want: stepSummaryHeaderNoAlert},
	}
	for _, tt := range tests {
		tt := tt
		t.Run(tt.name, func(t *testing.T) {
			t.Parallel()

			sample := sampleResultLog()
			sample.ResultSummary.Result = tt.result
			body, err := json.Marshal(sample)
			if err != nil {
				t.Fatalf("marshal sample: %v", err)
			}

			var stdout, stderr bytes.Buffer
			code, err := runReportStepSummary(context.Background(), nil, bytes.NewReader(body), &stdout, &stderr)
			if err != nil {
				t.Fatalf("runReportStepSummary: %v", err)
			}
			if code != 0 {
				t.Fatalf("exit code: got %d, want 0", code)
			}
			if !strings.Contains(stdout.String(), tt.want) {
				t.Fatalf("output:\n%s\nwant header %s", stdout.String(), tt.want)
			}
		})
	}
}

