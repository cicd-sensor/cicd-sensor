func TestRunReportStepSummary_Help(t *testing.T) {
	t.Parallel()

	var stdout, stderr bytes.Buffer
	code, err := runReportStepSummary(context.Background(), []string{"-h"}, nil, &stdout, &stderr)
	if err != nil {
		t.Fatalf("runReportStepSummary: got error %v, want nil", err)
	}
	if code != 0 {
		t.Fatalf("exit code: got %d, want 0", code)
	}
	if stdout.Len() != 0 {
		t.Fatalf("stdout: got %q, want empty", stdout.String())
	}
	for _, want := range []string{
		"usage: cicd-sensorctl report stepsummary [flags]",
		"--html-url",
		"--debug-url",
		"--health-failed",
	} {
		if !strings.Contains(stderr.String(), want) {
			t.Fatalf("stderr: got %q, want substring %q", stderr.String(), want)
		}
	}
}
